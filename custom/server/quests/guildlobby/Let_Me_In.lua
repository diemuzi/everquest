function event_say(e)
    -- Hail
    if (e.message:findi("hail")) then
        e.self:Say("Hello, " .. e.other:GetCleanName() .. ". Do you want access to all " .. eq.say_link("flags") .. " or " .. eq.say_link("zones") .. "?");
    end

    -- Flags
    if (e.message:findi("flags")) then
        for k, v in pairs(flags) do
            eq.set_global(tostring(k), tostring(v), 5, "F");
        end

        e.other:Message(15, "You now have all flags.");
    end

    -- Zones
    if (e.message:findi("zones")) then
        for _, v in ipairs(zones) do
            e.other:SetZoneFlag(v);
        end

        e.other:Message(15, "You now have access to all zones.");
    end
end

function event_trade(e)
    -- Require items library
    local item_lib = require("items");

    -- Return unused items
    item_lib.return_items(e.self, e.other, e.trade);
end